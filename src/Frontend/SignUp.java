    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        User.setName(txtName.getText().trim());
        User.setUserName(txtUserName.getText().trim());
        String pw = pwdPass.getText();
        String cp = pwdCon.getText();

        if (pw.equals(cp)) {
            User.setPassword(pw);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Check password again");
        }

        User.setSecurityQuestion(cmbSecurity.getSelectedItem().toString());
        User.setAnswer(txtAnswer.getText().trim());
        User.setType(type);
        DatabaseLayer.DBUserConnection up = new DBUserConnection();

        up.AddUser(User);
        JOptionPane.showMessageDialog(rootPane, "New User Added.");
        this.setVisible(false);
    }//GEN-LAST:event_btnAddActionPerformed


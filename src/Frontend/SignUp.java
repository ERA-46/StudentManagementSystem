    private void txtNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyReleased
        // TODO add your handling code here:
        String namePattern = "^[a-zA-Z\\s]{0,30}$";
        Pattern patt = Pattern.compile(namePattern);
        Matcher match = patt.matcher(txtName.getText());
        if (!match.matches()) {
            lblName.setText("Naming is incorrect");
        } else {
            lblName.setText(null);
        }
    }//GEN-LAST:event_txtNameKeyReleased

    private void txtUserNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserNameKeyReleased
        // TODO add your handling code here:
        String namePattern = "^[a-zA-Z0-9]{0,30}$";
        Pattern patt = Pattern.compile(namePattern);
        Matcher match = patt.matcher(txtUserName.getText());
        if (!match.matches()) {
            lblUserName.setText("Incorrect User name format");
        } else {
            lblUserName.setText(null);
        }
    }//GEN-LAST:event_txtUserNameKeyReleased


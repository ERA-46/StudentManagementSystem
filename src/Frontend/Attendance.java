    public void search() {
        DBConnection con = new DBConnection();

        try {
            String id = txtStudID.getText();
            String sql = "select Name,Grade FROM Student where Student_ID ='" + id + "'";
            PreparedStatement prep = con.getDBConnection().prepareStatement(sql);
            ResultSet rs = prep.executeQuery();

            if (rs.next()) {
                txtName.setText(rs.getString(1));
                txtGrade.setText(rs.getString(2));
                //jTextField3.setText(rs.getString(3));
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect Student ID");
            }
            prep.close();
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

